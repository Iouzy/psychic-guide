// Formatted translation: resolves {name} placeholders from `vars`.
function trf(pt, vars) {
  var s = tr(pt);
  if (vars) {
    for (var k in vars) {
      if (Object.prototype.hasOwnProperty.call(vars, k)) {
        s = s.split("{" + k + "}").join(String(vars[k]));
      }
    }
  }
  return s;
}

